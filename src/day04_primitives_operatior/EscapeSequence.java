package day04_primitives_operatior;

public class EscapeSequence {
  public static void main (String[] args) {
	  //print He Said "Hello".
	  System.out.println("He Said \"Hello\" .");
	  System.out.println("He also Said : \" Fuck this shit \"");
	  System.out.println( " \'hello\'");
	  System.out.print(" 3ala \t el \t mjweeeeeeeeeeeeeeeeeez  \n");
	  System.out.print("Would you like \' coffee\' or \' tea\' \n");
	  System.out.println("\"\\__/\"");
	  System.out.print("Java\\nSelenium");
	  // \ back slash is called escape char
  }
}
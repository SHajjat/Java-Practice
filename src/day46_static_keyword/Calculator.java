package day46_static_keyword;

public class Calculator {
	static double x;
	static double y;
	
	public static double plus(double x , double y){
		return x+y;
	}
	
	public static double minus(double x ,double y ){
		return x-y;
	}
	public static double multiply(double x ,double y ){
		return x*y;
	}
	
}

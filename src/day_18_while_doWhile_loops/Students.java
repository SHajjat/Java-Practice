package day_18_while_doWhile_loops;

public class Students {

	public static void main(String[] args) {
		int students = 1;
		
		while (students <=10) {
			System.out.println("number of students is "+students);
			students++;
		}
		students--;
		System.out.println("number of students is "+students + " no more spaces");
		

	}

}

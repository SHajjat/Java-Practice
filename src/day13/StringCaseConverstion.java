package day13;

public class StringCaseConverstion {
	public static void main(String[] args) {
		String city = "Amman";
		System.out.println(city);
		System.out.println(city.toUpperCase());//will make all letters upperCase
		System.out.println(city.toLowerCase());//will print all letters lowercase
		String lang = "JaVa";
		System.out.println(lang.toLowerCase());
	    System.out.println(lang);//just like below, so we are not changing original value
	    int n = 100;
	    System.out.println(n*5);
	    System.out.println(n);
	    lang =lang.toLowerCase();
	    System.out.println(lang);

	}

}
